#include <iostream>

int searchValue(int nums[], int size, int t) {
    /*
    Инициализация переменных:
    Устанавливаем начальные значения для переменных left и right. left указывает на начало массива, а right - на его конец.
    
    Цикл бинарного поиска:
    Входим в цикл бинарного поиска, который продолжается до тех пор, пока left не превысит right.
    На каждой итерации вычисляем средний индекс mid между left и right.
    
    Сравнение значения в mid с t:
    Сравниваем значение в индексе mid с искомым значением t.
    Если значение в mid равно t, возвращаем индекс mid, где значение найдено.
    
    Обновление left и right:
    Если значение в mid меньше t, сдвигаем left вправо на mid + 1, чтобы исключить левую часть массива.
    Если значение в mid больше t, сдвигаем right влево на mid - 1, чтобы исключить правую часть массива.
    
    Выход из цикла:
    Цикл продолжается, пока left не станет больше right.
    Если значение t не найдено в массиве, возвращаем -1.
    */
    int left = 0;
    int right = size - 1;

    while (left <= right) {
        int mid = left + (right - left) / 2;
        if (nums[mid] == t) {
            return mid;
        }
        else if (nums[mid] < t) {
            left = mid + 1;
        }
        else {
            right = mid - 1;
        }
    }

    return -1;
}

int main() {
    setlocale(LC_ALL, "Russian");
    int nums[] = { -1, 0, 3, 5, 9, 12 };
    int t = 9;
    int size = sizeof(nums) / sizeof(nums[0]);

    int result = searchValue(nums, size, t);

    if (result != -1) {
        std::cout << "Значение найдено в позиции: " << result << std::endl;
    }
    else {
        std::cout << "Значение не найдено." << std::endl;
    }

    return 0;
}



